const name = "Pratik Raj"
switch (name) {
    case "Raju":
        console.log("I am Raju")
        break;
    case "Ritik Raj":
        console.log("I am Ritik raj")
        break;
    case "Ram":
        console.log("I am Ram")
        break;

    default:
        console.log(`I am ${name}`)
        break;
}